public class Main {
    public static void main(String[] args) {
        System.out.println("Hello World!");


        //Declaration of variable x
        int x;

        //Initialization of variable x
        x = 15;

        /*
        This is multi line
        comment
         */
        System.out.println(x);

        //Initialization and Declaration at the same line
        int y = 10;
        System.out.println("y = " + y);

        //String data type
        String name = "Karina";
        System.out.println(name);

        //Double data type
        double temperature = 36.6d +1;
        System.out.println(temperature);

        String temperature2 = "36.6";
        System.out.println(temperature2);

        //Final variable -you can assign only one

        final double pie;
        pie = 3.14153443453452d;


    }
}
